package io.github.martinschneider.kommpeiler.examples;

public class K005a_IfElseConditions {
  public static void main(String[] args) {
    int a = 0;
    if (a == 1) {
      System.out.println("a=1");
    } else if (a == 2) {
      System.out.println("a=2");
    } else {
      System.out.println("a!=1 && a!=2");
    }
  }
}
